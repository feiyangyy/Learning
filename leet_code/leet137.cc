class Solution {
public:
  /// @brief 给你一个整数数组 nums ，除某个元素仅出现 一次
  /// 外，其余每个元素都恰出现 三次 。请你找出并返回那个只出现了一次的元素。
  /// 你必须设计并实现线性时间复杂度的算法且使用常数级空间来解决此问题。
  /// @param nums
  /// @return
  int singleNumber(vector<int> &nums) {
    std::vector<uint32_t> bits_count(32, 0);
    uint32_t tmp_val;
    for (int i = 0; i < nums.size(); ++i) {
      int j = 0;
      // 负数右移补1需注意
      // static
      // cast只能做到类型转换（可能会发生数值丢失问题），需要使用指针才能避免这种情况
      tmp_val = *(reinterpret_cast<uint32_t *>(&nums[i]));
      while (j < 32 && (tmp_val >> j)) {
        if ((tmp_val >> j) & 0x1) {
          bits_count[j]++;
        }
        ++j;
      }
    }
    int r = 0;
    for (int i = 0; i < 32; ++i) {
      if (bits_count[i] % 3) {
        r |= ((0x1) << i);
      }
    }
    return r;
  }
};

